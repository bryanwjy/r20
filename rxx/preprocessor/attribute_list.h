#define __RXX_ATOMIZE_ATTRIBUTE(NAME)                       \
    RXX_PASTE(RXX_APPEND_IS_EMPTY(__RXX_ATOMIZE_ATTRIBUTE_, \
        RXX_CONCAT(__RXX_ATTRIBUTE_TYPE_AGGREGATE_, NAME))(NAME))

#define __RXX_ATTRIBUTE_ATOMIZATION(ARGS) \
