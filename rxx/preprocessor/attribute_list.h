#  else
#    define __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_1(NAME) \
        __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_0(NAME)     \
        __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_2
#    define __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_2(NAME) \
        __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_0(NAME)     \
        __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_1
#  endif
#  define __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_1_END
#  define __RXX_TRANSLATE_MSVC_EXT_ATTRIBUTE_2_END

#  define __RXX_FILTER_MSVC_EXT_ATTRIBUTES(ARGS) \
      RXX_SWAP_CONCAT(_END, __RXX_FILTER_MSVC_EXT_ATTRIBUTES_1 ARGS)
#  define __RXX_FILTER_MSVC_EXT_ATTRIBUTES_1(NAME)                \
